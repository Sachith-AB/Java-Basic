public class for_loop {
    public static void main(String[] args) {
        // for loop

        for (int i = 0; i < 10; i++) {
            System.out.print(i + " ");
        }
        System.out.println("/n");
        for (int i = 10; i >= 1; i--) {
            System.out.println("Happy new year ");
        }
    }
}
